package Strings_Arrays_Streams;

public class AddSubsMultDiv_InSOP {
    public static void main(String args[]) {

      System.out.println(10 + 10+ "Harry");
      System.out.println("Harry" + 10 + 10);
      
      System.out.println(10 * 10+ "Harry");
      System.out.println("Harry" + 10 * 10);
      
      System.out.println(20 / 5 + "Harry");
      System.out.println("Harry" + 20 / 5);
      
      System.out.println(20 - 5 + "Harry");
//        System.out.println("Harry" + 20 - 5);
      //  It will throw error: bad operand types for binary operator '-'
    }
}